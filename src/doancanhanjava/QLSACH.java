    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:

        SACH x = null;
        String ma, xuatban, ten;
        double dG;
        int sl;
        Date ngaynhap;

        Date ngayNhap = new Date();
        SimpleDateFormat df = new SimpleDateFormat("dd/mm/yyyy");
        ten = tensachtxt.getText();

        ma = masachtxt.getText();
        xuatban = nhaxuatbantxt.getText();

        sl = Integer.parseInt(soluongtxt.getText());
        dG = Double.parseDouble(dongiatxt.getText());
        if (rdsachchuyennganh.isSelected()) {
            x = new SachChuyenNganh(ten, xuatban, ma, xuatban, ngayNhap, sl, dG, "sach chuyen nganh");

        }
        if (rdsachthamkhao.isSelected()) {
            x = new SachThamKhao(ma, ten, xuatban, ngayNhap, sl, dG, "sach tham khao");
        }
        a.them(x);
        dtm.addRow(new Object[]{
            x.getMasach(), x.getNhaxuatban(), x.getNgaynhap(), x.getSoluong(), x.getDongia(), x.getTen(), x.getLoai()});
    }//GEN-LAST:event_addActionPerformed

