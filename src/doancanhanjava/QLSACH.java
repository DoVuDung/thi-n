    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:

        JFileChooser fc = new JFileChooser();
        fc.showSaveDialog(this);

        String filePath = "/home/anonymous/Desktop/dacn.txt";
        File file = new File(filePath);
        try {
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);

            for (int i = 0; i < bang.getRowCount(); i++) {//rows
                for (int j = 0; j < bang.getColumnCount(); j++) {//columns
                    bw.write(bang.getValueAt(i, j).toString() + " ");
                }
                bw.newLine();
            }

            bw.close();
            fw.close();

        } catch (IOException ex) {
            Logger.getLogger(QLSACH.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_saveActionPerformed

